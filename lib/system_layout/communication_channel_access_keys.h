#ifndef COMMUNICATION_CHANNEL_ACCESS_KEYS_H
#define COMMUNICATION_CHANNEL_ACCESS_KEYS_H

// Enum for access keys
enum AccessKey {
    KEY_1,   // Represents key 1 access
    KEY_2,   // Represents key 2 access
    KEY_3,   // Represents key 3 access
    GOD_MODE // Represents god mode access, which has write access premissions to any channel
};

#endif  // COMMUNICATION_CHANNEL_ACCESS_KEYS_H